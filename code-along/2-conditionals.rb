# Booleans
temp = 32
precip = 100
# Boolean Expressions

# If Conditional Logic
if temp > 60 && precip < 60
    puts "Nice!"
# If/Else Conditional Logic
elsif temp > 60 && precip == 0
    puts "Very Nice"


# Elsif Conditional Logic
else 
    puts "worst"

end

# Combining Expressions