# Arrays are lists of things
not_so_favourite = ["mushrooms"]
# Accessing the array
favourite_foods = ["tacos", "anchos", "pizza"]
# Add to the array
favourite_foods.push ("dumplings")

#you can also end to the array by using "<<" sign 

# add arrays together 
combined_array = favourite_foods + not_so_favourite
# There are lots of fun things you can do with arrays:

#show me number of items in the list 
puts "the total number of items are #{combined_array.size}"
